numbers = [1,2,3]

print(numbers)

numbers.append(4)

print(numbers)                              # Adding a number in the list

numbers.extend([5,6])                      #  Adding multiple elements into the list

print(numbers)

numbers.insert(7,8)                        # In Insert(Index, element) 

print(numbers)

# Removing the elements from the list

numbers.remove(8)                           # Remove the element from the list 

print(numbers)

numbers.pop(0)                             # Pop removes the index value from the list

print(numbers)